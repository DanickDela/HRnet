function randomItem(array) {
  return array[Math.floor(Math.random() * array.length)];
}

function randomDate(startYear = 1950, endYear = 2023) {
  const start = new Date(startYear, 0, 1);
  const end = new Date(endYear, 11, 31);
  const date = new Date(
    start.getTime() + Math.random() * (end.getTime() - start.getTime()),
  );
  return date;
}

function randomZip() {
  return Math.floor(10000 + Math.random() * 90000).toString();
}

function randomStreet() {
  return `${Math.floor(Math.random() * 999)} ${randomItem(lastNames)} St`;
}

export const generateEmployees = (count = 100) => {
  return Array.from({ length: count }, (_, i) => ({
    id: (i + 1).toString(),
    firstName: randomItem(firstNames),
    lastName: randomItem(lastNames),
    // mise au format ISO de la date en supprimant la partie time
    startDate: randomDate(2015, 2024).toISOString().split("T")[0],
    department: randomItem(departments),
    // mise au format ISO de la date en supprimant la partie time
    dateOfBirth: randomDate(1950, 2000).toISOString().split("T")[0],
    street: randomStreet(),
    city: randomItem(cities),
    stateUS: randomItem(statesUS),
    zipCode: randomZip(),
  }));
};
